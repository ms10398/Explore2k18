$('.voguesta').click(function() {
  var buttonId = $(this).attr('id');
  $('.voguesta-container').removeClass('out');
  $('.voguesta-container').addClass(buttonId);
  $('body').addClass('modal-active');
})

$('.voguesta-container').click(function() {
  $(this).addClass('out');
  $('body').removeClass('modal-active');
});

